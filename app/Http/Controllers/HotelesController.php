            $hoteles = Hoteles::all();        
            return view('admin.hoteles')->with('hoteles',$hoteles);
        }else{
            $hotelUser = auth()->user()->hotel;
            $hoteles = DB::select("SELECT* FROM hotel h WHERE  h.nombre_hotel = '$hotelUser'"); 
